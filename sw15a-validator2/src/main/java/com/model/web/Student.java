package com.model.web;

 
public class Student {

	private String name;	// default null
	private int id;			// default 0
	
	public String getName() {
		return name;
	}
	public int getId() {
		return id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	
}
